int check_id(char* pass)
{
  char admin_pass[32];
  FILE* fp = fopen("password.txt", "r");
  if (fp == NULL)
  {
    printf("Error: password.txt not found\n");
    exit(1);
  }
  fscanf(fp, "%s", admin_pass);
  fclose(fp);
  
  if (strcmp(pass, admin_pass) == 0)
  {
    return 1;
  }

  return 0;
}

void fact() {
  system("cat fact.txt");
}

void prompt() {
  char input_pass[32];

  setbuf(stdout, NULL);
  setbuf(stdin, NULL);
  setbuf(stderr, NULL);

  printf("%s\n\n", VAULT);

  printf("Enter your password: ");
  gets(input_pass);

  if (check_id(input_pass) == 1)
  {
    printf("Access granted! Here is your buffalo fact:\n");
    fact();
  }
  else
  {
    printf("Access denied!\n");
  }
}

